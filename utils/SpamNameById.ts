export function SpamNameById (id : number) {
    switch(id){
        case 1:
            return "violence"
            
        case 2:
            return "unethical"
            
        case 3 : 
            return "Phishing"

        case 4:
            return "inviteLink"
            
        case 5:
            return "Nuisance"
            
        case 6 : 
            return "racism"
    
        default: 
            return "violence"

    }
}